// 既存のデータを読み込んで更新
function addDummyJobs() {
  const jobsPath = path.join(__dirname, '../data/jobs.json');
  const jobsData = JSON.parse(fs.readFileSync(jobsPath, 'utf8'));
  
  // 既存のIDをチェック
  const existingIds = new Set(jobsData.jobs.map(j => j.id));
  
  // 新しいダミー求人を追加
  const newJobs = dummyJobs.filter(job => !existingIds.has(job.id));
  jobsData.jobs.push(...newJobs);
  
  // ファイルに書き込み
  fs.writeFileSync(jobsPath, JSON.stringify(jobsData, null, 2));
  
  console.log(`Added ${newJobs.length} dummy jobs`);
  console.log('Total jobs:', jobsData.jobs.length);
}

